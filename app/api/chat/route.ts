function findTopLevels(data: number[], count: number): number[] {
  const peaks: number[] = [];
  
  for (let i = 2; i < data.length - 2; i++) {
    if (data[i] > data[i-1] && data[i] > data[i-2] && 
        data[i] > data[i+1] && data[i] > data[i+2]) {
      peaks.push(data[i]);
    }
  }
  
  return peaks.sort((a, b) => b - a).slice(0, count);
}
